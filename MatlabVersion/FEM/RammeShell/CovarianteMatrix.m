function R = CovarianteMatrix(a)
    % Initier [R]
    R=zeros(6,9);
    
    % Remplir [R]
    R(1,1:3)=a(1,1:3);
    R(2,4:6)=a(2,1:3);
    R(3,7:9)=a(3,1:3);
    
    R(4,1:3)=a(2,1:3);
    R(4,4:6)=a(1,1:3);
    
    R(5,4:6)=a(3,1:3);
    R(5,7:9)=a(2,1:3);
    
    R(6,1:3)=a(3,1:3);
    R(6,7:9)=a(1,1:3);
    
end