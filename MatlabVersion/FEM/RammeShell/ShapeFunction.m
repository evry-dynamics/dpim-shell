function [N] = ShapeFunction(s, t)
    N = zeros(8,1);
    N(1) = 0.25*(1-s)*(1-t)*(-s-t-1);
    N(2) = 0.25*(1+s)*(1-t)*(s-t-1);
    N(3) = 0.25*(1+s)*(1+t)*(s+t-1);
    N(4) = 0.25*(1-s)*(1+t)*(-s+t-1);

    N(5) = 0.5*(1-t)*(1+s)*(1-s);
    N(6) = 0.5*(1+s)*(1+t)*(1-t);
    N(7) = 0.5*(1+t)*(1+s)*(1-s);
    N(8) = 0.5*(1-s)*(1+t)*(1-t);

end